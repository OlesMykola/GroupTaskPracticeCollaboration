package Sureyya.week2;

public class SwampNumber {
    public static void main(String[] args) {


        int num1 = 2;
        int num2 = 22;

        num2 -= num1;
        num1 += num2;
        num2 -= num2;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);

    }
}
