package Daria.week2;

//Numbers -- Swap Numbers
//Swap two variable values without using a third variable



public class SwapNumbers {
    public static void main(String[] args) {

        int a = 34;
        int b = 105;

         a = a + b;
        System.out.println(a);
        System.out.println(b);


        b = a - b;
        a = a - b;
        System.out.println(a);
        System.out.println(b);

    }
}
